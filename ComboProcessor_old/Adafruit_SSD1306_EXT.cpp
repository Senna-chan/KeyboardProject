{
}

// constructor for hardware SPI - we indicate DataCommand, ChipSelect, Reset
Adafruit_SSD1306_EXT::Adafruit_SSD1306_EXT(Adafruit_MCP23008 &expender, int8_t DC, int8_t RST, int8_t CS) : Adafruit_GFX(SSD1306_EXT_LCDWIDTH, SSD1306_EXT_LCDHEIGHT) {
  dc = DC;
  rst = RST;
  cs = CS;
  this->expender = &expender;
}


void Adafruit_SSD1306_EXT::begin(uint8_t vccstate,  bool reset) {
  _vccstate = vccstate;

  // set pin directions
    pinMode(dc, OUTPUT);
    expender->pinMode(cs, OUTPUT);
#ifdef HAVE_PORTREG
    csport      = portOutputRegister(digitalPinToPort(cs));
    cspinmask   = digitalPinToBitMask(cs);
    dcport      = portOutputRegister(digitalPinToPort(dc));
    dcpinmask   = digitalPinToBitMask(dc);
#endif
      SPI.begin();
      SPI.beginTransaction(SPISettings(10000000, MSBFIRST, SPI_MODE0));
    
  if ((reset) && (rst >= 0)) {
    // Setup reset pin direction (used by both SPI and I2C)
    pinMode(rst, OUTPUT);
    digitalWrite(rst, HIGH);
    // VDD (3.3V) goes high at start, lets just chill for a ms
    delay(1);
    // bring reset low
    digitalWrite(rst, LOW);
    // wait 10ms
    delay(10);
    // bring out of reset
    digitalWrite(rst, HIGH);
    // turn on VCC (9V?)
  }

  // Init sequence
  ssd1306_command(SSD1306_EXT_DISPLAYOFF);                    // 0xAE
  ssd1306_command(SSD1306_EXT_SETDISPLAYCLOCKDIV);            // 0xD5
  ssd1306_command(0x80);                                  // the suggested ratio 0x80

  ssd1306_command(SSD1306_EXT_SETMULTIPLEX);                  // 0xA8
  ssd1306_command(SSD1306_EXT_LCDHEIGHT - 1);

  ssd1306_command(SSD1306_EXT_SETDISPLAYOFFSET);              // 0xD3
  ssd1306_command(0x0);                                   // no offset
  ssd1306_command(SSD1306_EXT_SETSTARTLINE | 0x0);            // line #0
  ssd1306_command(SSD1306_EXT_CHARGEPUMP);                    // 0x8D
  if (vccstate == SSD1306_EXT_EXTERNALVCC)
    { ssd1306_command(0x10); }
  else
    { ssd1306_command(0x14); }
  ssd1306_command(SSD1306_EXT_MEMORYMODE);                    // 0x20
  ssd1306_command(0x00);                                  // 0x0 act like ks0108
  ssd1306_command(SSD1306_EXT_SEGREMAP | 0x1);
  ssd1306_command(SSD1306_EXT_COMSCANDEC);

 #if defined SSD1306_EXT_128_32
  ssd1306_command(SSD1306_EXT_SETCOMPINS);                    // 0xDA
  ssd1306_command(0x02);
  ssd1306_command(SSD1306_EXT_SETCONTRAST);                   // 0x81
  ssd1306_command(0x8F);

#elif defined SSD1306_EXT_128_64
  ssd1306_command(SSD1306_EXT_SETCOMPINS);                    // 0xDA
  ssd1306_command(0x12);
  ssd1306_command(SSD1306_EXT_SETCONTRAST);                   // 0x81
  if (vccstate == SSD1306_EXT_EXTERNALVCC)
    { ssd1306_command(0x9F); }
  else
    { ssd1306_command(0xCF); }

#elif defined SSD1306_EXT_96_16
  ssd1306_command(SSD1306_EXT_SETCOMPINS);                    // 0xDA
  ssd1306_command(0x2);   //ada x12
  ssd1306_command(SSD1306_EXT_SETCONTRAST);                   // 0x81
  if (vccstate == SSD1306_EXT_EXTERNALVCC)
    { ssd1306_command(0x10); }
  else
    { ssd1306_command(0xAF); }

#endif

  ssd1306_command(SSD1306_EXT_SETPRECHARGE);                  // 0xd9
  if (vccstate == SSD1306_EXT_EXTERNALVCC)
    { ssd1306_command(0x22); }
  else
    { ssd1306_command(0xF1); }
  ssd1306_command(SSD1306_EXT_SETVCOMDETECT);                 // 0xDB
  ssd1306_command(0x40);
  ssd1306_command(SSD1306_EXT_DISPLAYALLON_RESUME);           // 0xA4
  ssd1306_command(SSD1306_EXT_NORMALDISPLAY);                 // 0xA6

  ssd1306_command(SSD1306_EXT_DEACTIVATE_SCROLL);

  ssd1306_command(SSD1306_EXT_DISPLAYON);//--turn on oled panel
}


void Adafruit_SSD1306_EXT::invertDisplay(uint8_t i) {
  if (i) {
    ssd1306_command(SSD1306_EXT_INVERTDISPLAY);
  } else {
    ssd1306_command(SSD1306_EXT_NORMALDISPLAY);
  }
}

void Adafruit_SSD1306_EXT::ssd1306_command(uint8_t c) {
    // SPI
	expender->pinMode(cs, OUTPUT);
	expender->digitalWrite(cs, HIGH);
    digitalWrite(dc, LOW);
	expender->digitalWrite(cs, LOW);

    fastSPIwrite(c);
	expender->pinMode(cs, OUTPUT);
	expender->digitalWrite(cs, HIGH);
}

