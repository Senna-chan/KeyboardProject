#define SSD1306_EXT_swap(a, b) { int16_t t = a; a = b; b = t; }

// the most basic function, set a single pixel
void Adafruit_SSD1306_EXT::drawPixel(int16_t x, int16_t y, uint16_t color) {
  if ((x < 0) || (x >= width()) || (y < 0) || (y >= height()))
    return;

  // check rotation, move pixel around if necessary
  switch (getRotation()) {
  case 1:
    SSD1306_EXT_swap(x, y);
    x = WIDTH - x - 1;
    break;
  case 2:
    x = WIDTH - x - 1;
    y = HEIGHT - y - 1;
    break;
  case 3:
    SSD1306_EXT_swap(x, y);
    y = HEIGHT - y - 1;
    break;
  }

  // x is which column
    switch (color)
    {
      case WHITE_EXT:   buffer[x+ (y/8)*SSD1306_EXT_LCDWIDTH] |=  (1 << (y&7)); break;
      case BLACK_EXT:   buffer[x+ (y/8)*SSD1306_EXT_LCDWIDTH] &= ~(1 << (y&7)); break;
      case INVERSE_EXT: buffer[x+ (y/8)*SSD1306_EXT_LCDWIDTH] ^=  (1 << (y&7)); break;
    }

}

